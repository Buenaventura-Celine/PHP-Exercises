The Polytechnic University of the Philippines (PUP) is a government educational institution governed 
by Republic Act Number 8292 known as the Higher Education Modernization Act of 1997, and its Implementing
Rules and Regulations contained in the Commission on Higher Education Memorandum Circular No. 4, series
1997. PUP is one of the country's highly competent educational institutions. The PUP Community 
is composed of the Board of Regents, University Officials, Administrative and Academic Personnel, 
Students, various Organizations, and the Alumni.